print("PROGRAMA PARA CALCULAR LA DISTANCIA RECORRIDA\n")


velocidad = float(input("Ingresa la velocidad en kilómetros por hora:"))

tiempo_horas = float(input("Ingresa el tiempo del recorrido en horas:"))

# Distancia usando la fórmula x = v * t
distancia = velocidad * tiempo_horas

print(f"\nLa distancia recorrida fueron {distancia} kilómetros\n")
