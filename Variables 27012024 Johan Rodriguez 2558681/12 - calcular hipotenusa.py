print("PROGRAMA QUE PERMITE CALCULAR LA HIPOTENUSA DE UN TRIANGULO RECTANGULO\n")

cateto_a = float(input("Ingresa la longitud del cateto a:"))
cateto_b = float(input("Ingresa la longitud del cateto b:"))

hipotenusa = (cateto_a**2 + cateto_b**2)**0.5

print(f"\nLa hipotenusa del triángulo rectángulo es: {hipotenusa}\n")
